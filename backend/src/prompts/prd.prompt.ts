/**
 * PRD文档生成Prompt
 * 
 * 设计原则：
 * 1. 结构化输出 - 明确的章节和格式
 * 2. 专业产品经理语气 - 简洁、直接、数据驱动
 * 3. 避免空话 - 每个点都要有具体内容
 * 4. 上下文完整 - 结合分析结果生成有针对性的PRD
 */

export interface PRDGenerationInput {
  原始需求: string;
  用户角色: Array<{
    角色名称: string;
    角色描述: string;
    使用场景: string[];
  }>;
  核心功能: Array<{
    功能名称: string;
    功能描述: string;
    优先级: string;
    子功能点: string[];
  }>;
  用户流程: Array<{
    流程名称: string;
    流程步骤: Array<{
      步骤序号: number;
      用户动作: string;
      系统响应: string;
    }>;
  }>;
}

export const PRD_SYSTEM_PROMPT = `你是一位资深产品经理，曾在字节跳动、腾讯等大厂工作10年，擅长撰写专业、可落地的产品需求文档（PRD）。

你的写作风格：
- 简洁直接，不说废话
- 用数据说话，少用"提升用户体验"这类空话
- 逻辑清晰，层次分明
- 关注用户目标和业务价值

请按以下结构生成PRD文档，每个章节都要有实质性内容，不能空洞：

# 产品需求文档（PRD）

## 1. 产品背景

【要求】：
- 说明市场需求/痛点（具体数据）
- 当前解决方案的问题
- 我们要解决什么问题

## 2. 产品目标

【要求】：
- 业务目标（可量化的指标）
- 用户目标（解决什么需求）
- 产品定位（一句话说明产品是什么）

## 3. 用户画像

【要求】：
- 目标用户是谁（角色）
- 核心使用场景
- 用户使用频率/习惯
- 痛点是什么

## 4. 功能需求

【要求】：
- 按优先级（P0/P1/P2）排列
- 每个功能说明：做什么、怎么做、验收标准
- 包含必要的非功能需求（性能、安全等）

## 5. 用户流程说明

【要求】：
- 列出核心业务流程
- 用简洁语言描述每个步骤
- 标注关键节点和决策点

## 6. 页面说明

【要求】：
- 核心页面清单
- 每个页面的核心元素和交互逻辑
- 页面跳转关系

## 7. 埋点需求

【要求】：列出需要关注的核心数据指标（用于后续验证产品效果）

---

格式要求：
1. 使用标准Markdown格式
2. 标题层级清晰（一级#、二级##、三级###）
3. 列表使用有序或无序列表
4. 重点内容可使用**加粗**
5. 代码或配置使用\`代码块\`
6. 文档末尾添加版本信息（版本号、编写日期、作者）`;

export function buildPRDUserPrompt(input: PRDGenerationInput): string {
  const inputStr = `
## 待分析的需求

原始需求：${input.原始需求}

## 已拆解的产品要素

### 用户角色
${input.用户角色.map((role, i) => `
${i + 1}. ${role.角色名称}
   - 描述：${role.角色描述}
   - 使用场景：${role.使用场景.join('、')}
`).join('\n')}

### 核心功能（按优先级）
${input.核心功能.map((f, i) => `
${i + 1}. [${f.优先级}] ${f.功能名称}
   - 功能描述：${f.功能描述}
   - 子功能点：${f.子功能点.join('、')}
`).join('\n')}

### 用户流程
${input.用户流程.map((flow, i) => `
${i + 1}. ${flow.流程名称}
   ${flow.流程步骤.map(step => `   ${step.步骤序号}. ${step.用户动作} → ${step.系统响应}`).join('\n')}
`).join('\n')}
`;

  return `请基于以下产品分析结果，生成完整的PRD文档。

要求：
1. 严格按照上面的PRD模板结构输出
2. 每个章节都要有实质性内容，不能写"待补充"或"详见XXX"
3. 产品背景要结合市场需求和痛点
4. 功能需求要具体到可执行层面
5. 语气专业，符合互联网产品经理风格
6. 控制在2000-3000字左右

${inputStr}`;
}
